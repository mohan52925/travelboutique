    // Initialize Swiper
    var swiper = new Swiper('.swiper-container', {
      pagination: {
        el: '.swiper-pagination',
      },
      navigation: {
        nextEl: '.swiper-button-next',
        prevEl: '.swiper-button-prev',
      },
      on: {
        slideChangeTransitionStart: function() {
          // Mute the video when transitioning to a new slide
          var videos = this.slides[this.activeIndex].querySelectorAll('video');
          videos.forEach(function(video) {
            video.muted = false;
          });
        },
        slideChangeTransitionEnd: function() {
          // Unmute the video when the transition ends
          var videos = this.slides[this.activeIndex].querySelectorAll('video');
          videos.forEach(function(video) {
            video.muted = false;
          });
        }
      }
    });
